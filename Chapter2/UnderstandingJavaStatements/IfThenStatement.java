
public class IfThenStatement {
	
	public static void main(String[] args){
		int hourOfDay = 0;
		int morningGreetingCount = 0;
		
		if(hourOfDay < 11){
			System.out.println("Good Morning");
		}
		
		if(hourOfDay < 11){
			System.out.println("Good Morning");
			morningGreetingCount++;
		}
		
	}

}
